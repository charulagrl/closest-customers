import math

class MathsConversions(object):

	@classmethod
	def get_radian(self, value):
		return math.radians(value)

	@classmethod
	def get_cos(self, value):
		return math.cos(value)

	@classmethod
	def get_sin(self, value):
		return math.sin(value)

	@classmethod
	def get_acos(self, value):
		return math.acos(value)
